void GunJon()
{
    ll n, k;
    cin >> n >> k;
    vector<ll> v;
    cin >> n >> k;
    for (ll i = 1; i * i <= n; i++)
    {
        if (n % i == 0)
        {
            v.push_back(i);
            if (i * i != n)
                v.push_back(n / i);
        }
    }
    sort(it(v));
    (k > v.size()) ? cout << -1 << nl : cout << v[k - 1] << nl;
}
